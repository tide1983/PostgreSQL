-- 5. Название треков, которые содержат слово «мой» или «my» (ИСПРАВЛЕННАЯ ВЕРСИЯ)
SELECT 
    название, 
    длительность 
FROM треки 
WHERE 
    -- Для русского слова "мой"
    (название ILIKE 'мой %' OR          -- слово в начале
     название ILIKE '% мой %' OR        -- слово в середине  
     название ILIKE '% мой' OR          -- слово в конце
     название = 'мой') OR               -- только это слово
    -- Для английского слова "my"
    (название ILIKE 'my %' OR           -- слово в начале
     название ILIKE '% my %' OR         -- слово в середине
     название ILIKE '% my' OR           -- слово в конце
     название = 'my')                   -- только это слово
ORDER BY название;

-- ДОПОЛНИТЕЛЬНЫЕ ВАРИАНТЫ (для демонстрации знаний):

-- Вариант 2: С использованием массивов и string_to_array
SELECT 
    название, 
    длительность 
FROM треки 
WHERE 
    string_to_array(lower(название), ' ') && ARRAY['мой', 'my']
ORDER BY название;

-- Вариант 3: С использованием регулярных выражений
SELECT 
    название, 
    длительность 
FROM треки 
WHERE 
    название ~* '\yмой\y' OR 
    название ~* '\ymy\y'
ORDER BY название;